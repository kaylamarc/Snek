
public class Rect {
	double x, y, width, height;
//	public Direction direction;
	
	/**
	 * rectangle object
	 * @param x
	 * @param y
	 * @param width
	 * @param height
	 */
	public Rect(double x, double y, double width, double height) {
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
	
	
}
